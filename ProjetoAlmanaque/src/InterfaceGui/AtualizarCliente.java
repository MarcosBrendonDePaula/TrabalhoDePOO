    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (MainGui.Clientes.getCliente(edtbuscacliente.getText()) != null) {
            JOptionPane.showMessageDialog(this, "Cliente encontrado");
        } else {
            JOptionPane.showMessageDialog(this, "Cliente não encontado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    /**
     * Atualiza o nome do cliente
     * @param evt 
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (MainGui.Clientes.getCliente(edtbuscacliente.getText()) != null) {
            MainGui.Clientes.getCliente(edtbuscacliente.getText()).setNome(edtnovonome.getText());
            JOptionPane.showMessageDialog(this, "Nome do cliente atualizado");
        } else {
            JOptionPane.showMessageDialog(this, "Nome do cliente não atualizado");
        }
    }//GEN-LAST:event_jButton2ActionPerformed
