/**
 * Método para atualizar o cpf
 * @param evt 
 */
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if (MainGui.Clientes.getCliente(edtbuscacliente.getText()) != null) {
            if(edtnovoemail.getText().contains("@")){
                MainGui.Clientes.getCliente(edtbuscacliente.getText()).setEmail(edtnovoemail.getText());
            JOptionPane.showMessageDialog(this, "Email do cliente atualizado");
            }else
            JOptionPane.showMessageDialog(this, "Email do cliente não atualizado cheque o email");
        } else {
            JOptionPane.showMessageDialog(this, "Email do cliente não atualizado");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

