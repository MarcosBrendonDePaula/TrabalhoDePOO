    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(edtesqueceu.getText().equals("") || edtesqueceu2.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha Todos os Campos!");
        }else{
            Funcionario f1= MainGui.Funcionarios.buscaGet(edtesqueceu.getText(),edtesqueceu2.getText());
            if(f1!=null){
                JOptionPane.showMessageDialog(this,"Nome: "+f1.getNome()+"\nCargo: "+f1.getCargo()+"\nCPF: "+f1.getCPF(),"Informações", JOptionPane.INFORMATION_MESSAGE); 
            }else{
                JOptionPane.showMessageDialog(this,"Este Funcionário não está Cadastrado!");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
   
