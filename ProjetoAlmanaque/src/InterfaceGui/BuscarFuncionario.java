    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(edtbuscacpf==null){
            JOptionPane.showMessageDialog(this,"Preencha o Campo CPF!");
        }else{
            Funcionario f= MainGui.Funcionarios.buscaCPF(edtbuscacpf.getText());
            if(f!=null){
                JOptionPane.showMessageDialog(this,"Nome: "+f.getNome()+"\nCargo: "+f.getCargo()+"\nCPF: "+f.getCPF(),"Informações", JOptionPane.DEFAULT_OPTION);         
            }else{
                JOptionPane.showMessageDialog(this,"Este Funcionário não está Cadastrado!");
            }
        }
        
  
    }//GEN-LAST:event_jButton1ActionPerformed

