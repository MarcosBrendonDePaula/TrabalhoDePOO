    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(edtnome.getText().equals("")  || edtsobrenome.getText().equals("")  || edtcurso.getText().equals("")  || edtcpf.getText().equals("")  || edttelefone.getText().equals("") || !edtemail.getText().contains("@")){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente!");
        }else{
            Cliente novoc = new Cliente(edtnome.getText(), edtsobrenome.getText(), edtcpf.getText(), edtcurso.getText(), edtemail.getText(), edttelefone.getText());
            MainGui.Clientes.add(novoc);
            JOptionPane.showMessageDialog(this, "Cliente Cadastrado");
            edtcpf.setText("");
            edtcurso.setText("");
            edtemail.setText("");
            edtnome.setText("");
            edtsobrenome.setText("");
            edttelefone.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

